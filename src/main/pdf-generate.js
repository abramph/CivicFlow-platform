/**
 * PDF generation via Electron's webContents.printToPDF.
 * Uses hidden BrowserWindow + HTML templates (no PDFKit/font packaging issues).
 */
const { BrowserWindow, app, dialog } = require('electron');
const fs = require('node:fs');
const path = require('node:path');
const { getBranding } = require('./branding.js');
const { APP_NAME } = require('../shared/appConfig.js');
const { error: logError } = require('./logger.js');

function buildReceiptHtml(data) {
  const { orgName, logoFileUrl, receiptId, date, paymentType, amount, memberName, memberEmail, eventName, campaignName, note } = data;
  const logoImg = logoFileUrl ? '<img src="' + logoFileUrl + '" alt="Logo" style="height:48px;max-width:120px;object-fit:contain;" />' : '';
  const o = escapeHtml(orgName || APP_NAME);
  const parts = [
    '<!DOCTYPE html><html><head><meta charset="UTF-8"><style>',
    '* { margin: 0; padding: 0; box-sizing: border-box; }',
    'body { font-family: -apple-system, BlinkMacSystemFont, \'Segoe UI\', Roboto, Arial, sans-serif; font-size: 14px; color: #1e293b; padding: 40px; line-height: 1.5; }',
    '.header { display: flex; align-items: center; gap: 16px; margin-bottom: 24px; border-bottom: 1px solid #e2e8f0; padding-bottom: 16px; }',
    '.org-name { font-size: 18px; font-weight: 600; color: #0f766e; }',
    '.subtitle { font-size: 12px; color: #64748b; margin-top: 4px; }',
    'h2 { font-size: 16px; font-weight: 600; margin-bottom: 16px; color: #0f172a; }',
    '.row { margin-bottom: 8px; }',
    '.footer { margin-top: 32px; padding-top: 16px; border-top: 1px solid #e2e8f0; font-size: 11px; color: #94a3b8; }',
    '</style></head><body>',
    '<div class="header">' + logoImg + '<div><div class="org-name">' + o + '</div><div class="subtitle">Community-Based Organization</div></div></div>',
    '<h2>Payment Receipt</h2>',
    '<div class="row"><strong>Receipt ID:</strong> ' + escapeHtml(String(receiptId ?? '')) + '</div>',
    '<div class="row"><strong>Date:</strong> ' + escapeHtml(String(date ?? '—')) + '</div>',
    '<div class="row"><strong>Payment type:</strong> ' + escapeHtml(String((paymentType ?? '').replace(/_/g, ' '))) + '</div>',
    '<div class="row"><strong>Amount:</strong> $' + (Number(amount ?? 0) / 100).toFixed(2) + '</div>',
  ];
  if (memberName) parts.push('<div class="row" style="margin-top:12px;"><strong>Member:</strong> ' + escapeHtml(memberName) + (memberEmail ? ' (' + escapeHtml(memberEmail) + ')' : '') + '</div>');
  if (eventName) parts.push('<div class="row"><strong>Event:</strong> ' + escapeHtml(eventName) + '</div>');
  if (campaignName) parts.push('<div class="row"><strong>Campaign:</strong> ' + escapeHtml(campaignName) + '</div>');
  if (note) parts.push('<div class="row"><strong>Note:</strong> ' + escapeHtml(note) + '</div>');
  parts.push('<div class="footer">Generated on ' + new Date().toISOString().slice(0, 19).replace('T', ' ') + ' UTC. Generated by ' + o + '.</div>');
  parts.push('</body></html>');
  return parts.join('');
}

function buildReportHtml(data) {
  const { orgName, logoFileUrl, title, subtitle, rows, columns } = data;
  const logoImg = logoFileUrl ? '<img src="' + logoFileUrl + '" alt="Logo" style="height:48px;max-width:120px;object-fit:contain;" />' : '';
  const ths = (columns || []).map((c) => '<th style="text-align:left;padding:8px 12px;border-bottom:2px solid #0f766e;color:#0f172a;">' + escapeHtml(c) + '</th>').join('');
  const trs = (rows || []).map((r) => {
    const cells = (Array.isArray(r) ? r : Object.values(r)).map((v) => '<td style="padding:8px 12px;border-bottom:1px solid #e2e8f0;">' + escapeHtml(String(v ?? '')) + '</td>').join('');
    return '<tr>' + cells + '</tr>';
  }).join('');
  const o = escapeHtml(orgName || APP_NAME);
  const t = escapeHtml(title || 'Report');
  const sub = subtitle ? '<div class="report-subtitle">' + escapeHtml(subtitle) + '</div>' : '';
  const footer = 'Generated on ' + new Date().toISOString().slice(0, 19).replace('T', ' ') + ' UTC. Generated by ' + o + '.';
  return '<!DOCTYPE html><html><head><meta charset="UTF-8"><style>' +
    '* { margin: 0; padding: 0; box-sizing: border-box; }' +
    'body { font-family: -apple-system, BlinkMacSystemFont, \'Segoe UI\', Roboto, Arial, sans-serif; font-size: 14px; color: #1e293b; padding: 40px; line-height: 1.5; }' +
    '.header { display: flex; align-items: center; gap: 16px; margin-bottom: 24px; border-bottom: 1px solid #e2e8f0; padding-bottom: 16px; }' +
    '.org-name { font-size: 18px; font-weight: 600; color: #0f766e; }' +
    '.subtitle { font-size: 12px; color: #64748b; margin-top: 4px; }' +
    'h2 { font-size: 16px; font-weight: 600; margin-bottom: 8px; color: #0f172a; }' +
    '.report-subtitle { font-size: 12px; color: #64748b; margin-bottom: 16px; }' +
    'table { width: 100%; border-collapse: collapse; }' +
    '.footer { margin-top: 32px; padding-top: 16px; border-top: 1px solid #e2e8f0; font-size: 11px; color: #94a3b8; }' +
    '</style></head><body>' +
    '<div class="header">' + logoImg + '<div><div class="org-name">' + o + '</div><div class="subtitle">Community-Based Organization</div></div></div>' +
    '<h2>' + t + '</h2>' + sub +
    '<table><thead><tr>' + ths + '</tr></thead><tbody>' + trs + '</tbody></table>' +
    '<div class="footer">' + footer + '</div></body></html>';
}

function escapeHtml(s) {
  if (s == null) return '';
  const str = String(s);
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function getLogoDataUrl(logoPath) {
  if (!logoPath || !fs.existsSync(logoPath)) return null;
  try {
    const buf = fs.readFileSync(logoPath);
    const base64 = buf.toString('base64');
    return `data:image/png;base64,${base64}`;
  } catch {
    return null;
  }
}

/**
 * Generate PDF via hidden BrowserWindow + printToPDF.
 * @param {object} opts - { template: 'receipt'|'report', data: {...}, defaultFilename: string, showSaveDialog?: boolean }
 * @returns {Promise<{ok: boolean, path?: string, error?: string, stack?: string, canceled?: boolean}>}
 */
async function generatePdf(opts) {
  const { template, data, defaultFilename = 'document.pdf', showSaveDialog = true } = opts || {};
  let win = null;
  try {
    if (!template || !data) {
      const err = new Error('Missing template or data');
      logError('PDF generate [pdf-generation]:', err?.message, err?.stack);
      return { ok: false, error: err.message, stack: err.stack, context: 'pdf-generation' };
    }

    const branding = getBranding();
    const orgName = data.organizationName ?? branding?.cboName ?? data.orgName ?? APP_NAME;
    const logoPath = branding?.logoPath ?? data.logoPath ?? null;
    const logoFileUrl = getLogoDataUrl(logoPath);

    let html;
    if (template === 'receipt') {
      html = buildReceiptHtml({ ...data, orgName, logoFileUrl });
    } else if (template === 'report') {
      html = buildReportHtml({ ...data, orgName, logoFileUrl });
    } else {
      const err = new Error('Unknown template: ' + template);
      logError('PDF generate [pdf-generation]:', err?.message, err?.stack);
      return { ok: false, error: err.message, stack: err.stack, context: 'pdf-generation' };
    }

    const dataUrl = `data:text/html;charset=utf-8,${encodeURIComponent(html)}`;

    win = new BrowserWindow({
      width: 800,
      height: 600,
      show: false,
      webPreferences: {
        nodeIntegration: false,
        contextIsolation: true,
        sandbox: true,
      },
    });

    await win.loadURL(dataUrl);

    await new Promise((resolve, reject) => {
      win.webContents.once('did-finish-load', resolve);
      win.webContents.once('did-fail-load', (_, code, desc) => reject(new Error('Load failed: ' + code + ' ' + desc)));
      setTimeout(() => resolve(), 1000);
    });

    const margins = { top: 0.5, bottom: 0.5, left: 0.5, right: 0.5 };
    const validateMargins = (m) =>
      m.top <= 1 && m.bottom <= 1 && m.left <= 1 && m.right <= 1;
    if (!validateMargins(margins)) {
      const err = new Error('Invalid margins: must be <= 1 inch each');
      logError('PDF margins validation [pdf-generation]:', err?.message, err?.stack);
      win.close();
      return { ok: false, error: err.message, stack: err.stack, context: 'pdf-generation' };
    }

    const pdfOptions = {
      pageSize: 'A4',
      printBackground: true,
      margins,
    };

    const pdfBuffer = await win.webContents.printToPDF(pdfOptions);

    win.close();
    win = null;

    if (!showSaveDialog) {
      return { ok: true, buffer: pdfBuffer };
    }

    const result = await dialog.showSaveDialog({
      title: 'Save PDF',
      defaultPath: defaultFilename,
      filters: [{ name: 'PDF', extensions: ['pdf'] }],
    });
    if (result.canceled || !result.filePath) {
      return { ok: false, canceled: true };
    }

    fs.writeFileSync(result.filePath, pdfBuffer);
    return { ok: true, path: result.filePath };
  } catch (err) {
    if (win && !win.isDestroyed()) win.close();
    const msg = err?.message ?? String(err);
    const stack = err?.stack ?? '';
    logError('PDF generate failed [pdf-generation]:', msg, stack);
    return { ok: false, error: msg, stack, context: 'pdf-generation' };
  }
}

module.exports = { generatePdf };
